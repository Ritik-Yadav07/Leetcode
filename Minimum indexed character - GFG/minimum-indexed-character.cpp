class Solution
{
  public:
    //Function to find the minimum indexed character.
    int minIndexChar(string str, string patt)
    {
        // Your code here
        vector<int>hash(26,0);
        for(auto &ch : patt)
        {
            hash[ch-'a']++;
        }
        
        for(int i=0;i<str.size();i++)
        {
            if(hash[str[i]-'a']>=1) return i;
        }
        
        return -1;
    }
};

