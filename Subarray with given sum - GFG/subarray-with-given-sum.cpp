class Solution
{
    public:
    //Function to find a continuous sub-array which adds up to a given number.
    vector<int> subarraySum(vector<int>arr, int n, long long s)
    {
        // Your code here
        long long curr_sum = 0;
        int head=0;
        int tail = 0;
        if(s==0) return {-1};
        for(head=0;head<n;head++){
            
            curr_sum+=arr[head];
            while(curr_sum>s){
                curr_sum-=arr[tail];
                tail++;
            }
            if(curr_sum==s){
                return {tail+1,head+1};
            }
        }
        return {-1};
    }
};

